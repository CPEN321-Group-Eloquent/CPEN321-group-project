package com.example.present;

public class Content {
    public String font;
    public String style;
    public int size;
    public int colour;
    public String message;

    public Content(String font, String style, int size, int colour, String message) {
        this.font = font;
        this.style = style;
        this.size = size;
        this.colour = colour;
        this.message = message;
    }
}
