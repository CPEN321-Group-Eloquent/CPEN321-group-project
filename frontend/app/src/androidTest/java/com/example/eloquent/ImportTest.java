import org.junit.runner.RunWith;

@LargeTest
@RunWith(AndroidJUnit4.class)
public class ImportTest {

    @Rule
    public ActivityScenarioRule<Login> mActivityScenarioRule =
            new ActivityScenarioRule<>(Login.class);

    @Test
    public void test() {
        ViewInteraction appCompatButton = onView(
                allOf(withId(R.id.LB), withText("Dev Login"),
                        childAtPosition(
                                allOf(withId(R.id.loginButtons),
                                        childAtPosition(
                                                withClassName(is("androidx.constraintlayout.widget.ConstraintLayout")),
                                                2)),
                                0),
                        isDisplayed()));
        appCompatButton.perform(click());
    }

    private static Matcher<View> childAtPosition(
            final Matcher<View> parentMatcher, final int position) {

        return new TypeSafeMatcher<View>() {
            @Override
            public void describeTo(Description description) {
                description.appendText("Child at position " + position + " in parent ");
                parentMatcher.describeTo(description);
            }

            @Override
            public boolean matchesSafely(View view) {
                ViewParent parent = view.getParent();
                return parent instanceof ViewGroup && parentMatcher.matches(parent)
                        && view.equals(((ViewGroup) parent).getChildAt(position));
            }
        };
    }

    static Intent intent;
    static {
        intent = new Intent(ApplicationProvider.getApplicationContext(), MainActivity.class);
        Bundle bundle = new Bundle();
        String presentation = String.valueOf(R.string.sample_import_input);
        bundle.putString("presentation", presentation);
        intent.putExtras(bundle);
    }

    public void DisplayedImportedPresentation() {

        // Assert that the data we set up above is shown.
        onData(withId(R.id.listOfPres)).atPosition(0)
                .check(matches(hasDescendant(withText("Sample Presentation"))));
    }
}
